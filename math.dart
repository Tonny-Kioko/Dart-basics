void addNumbers(int n1, int n2){
  int result = n1 + n2;
  print(result);
}

void minusNumbers(int n1, int n2){
  int result = n1 - n2;
  print(result);
}

void multiplyNumbers(int n1, int n2){
  int result = n1 * n2;
  print(result);
}

void dividenumber(int n1, int n2){
  double result = n1 / n2;
  print(result);
}

void modulus(int n1, int n2){
  int result = n1 % n2;
  print(result);
}

